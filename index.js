/* Operadores Lógicos */

console.log( 5 == 5 && 6 == 6 ) // true
console.log( 5 == 5 && 6 != 6 ) // false

console.log( 5 != 5 || 6 == 6 ) // true
console.log( 5 == 5 || 6 != 6 ) // true

console.log( !(5 > 6) ) // true
console.log( !(5 < 6) ) // false

/* Operadores Aritmétricos */

console.log( 2 + 2 )    // 4
console.log( 2 - 2 )    // 0

console.log( 2 * 2 )    // 4
console.log( 2 / 2 )    // 1

console.log( 2 % 1.5 )  // 0.5